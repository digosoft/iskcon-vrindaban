                        <h1 class="entry-title">The Boat Festival In ISKCON Vrindaban</h1>
                        <div class="one_half"> 
  
                            <p  style="font-size: 15px; text-align: justify;">The Boat Festival has its roots in Goloka Vrindavan, the spiritual eternal realm. One of the boat pastimes is described in detail in Gopal Champu by Srila Jiva Goswami. One day the gopis, loaded with pots full of yogurt and butter, came to the banks of Manasi Ganaga, wishing to cross the river. They met a boatman who gladly agreed to help. When the boat was already far away from the bank the boatman stopped rowing, saying that He was too hungry. The gopis gave Him whatever food they had in their pots so that He could continue rowing. He continued. But after some time He stopped again. The gopis asked for what reason He was stopping. The boatman said that now He was too tired and that they had to massage His arms. Having heard this, the gopis told Him that they would throw Him out of the boat if He would not continue rowing. Suddenly clouds covered the sky, wind started to blow and the waters became very agitated. The boat started shaking from one side to another. The gopis were overwhelmed with fear. Radharani, searching for shelter, clung to the boatman. When She saw that He had a flute, She immediately realized that the boatman was none other than Her beloved Krishna. Then She showed the flute to the gopis to make them understand. They were very happy to spend the rest of their time in the company of Krishna.<br><br>

                            On 7th March 2018, the devotees of the International Society for Krishna Consciousness celebrated the festival in the courtyard of the Krishna-Balarama temple. Any festival celebrated in the temple originates from an eternal pastime performed by Krishna and His close associates.<br><br>

                            We inquired from His Grace Mukunda Datta Prabhu, head pujari of the Krishna-Balarama temple in Vrindavan about the significance of the Boat Festival:<br><br>   Aradhika Radha Dasi: “Is it true that this boat festival originates from the lila which happened in the waters of the Manasi Ganga?”<br><br>

                           Mukunda Datta Prabhu: “Yes, not only in Manasi Ganga, but also in the Jamuna River. Both places. Manasi Ganga or Ganga Devi has manifested from the mind of Krishna.  Now there is only a pond there, at Govardhan, but before, it was a flowing river. It is a very beautiful pastime of loving exchange between Krishna and His most dear devotees, the gopis. 

 
                            </p>
