                    <p  style="font-size: 15px; text-align: justify;">The Boat Festival is one of the most wonderful and colorful festivals celebrated in the Krishna-Balarama temple. When the temple closes at 1pm, preparations start. For the next three hours devotees make decorations inside the temple room. The whole courtyard gets filled up with water. It reminds one of a beautiful pond which has small ghats in all four directions. When the pond is ready, the devotees cover the waters with colorful flower petals, creating marvelous ornaments. There is a secret how the petals of different colors do not get mixed up together. In order to understand it one has to come many hours before and watch this art. Some devotees decorate the courtyard while others prepare the boat. In the past, the boat had different forms such as peacock or swan. This year the boat is shaped like the transcendental fish Matsya.<br><br> 


					At 4:30pm the devotees are already gathered, waiting for the beginning of the festival procession. At 5pm all three altars open, and Sri Radha-Syamasudara take Their seat in the beautiful Matsya boat. The procession starts. The sounds of melodious kirtan by His Grace Panchagauda Prabhu and devotees fill up the air. Their Lordships are enjoying the ride. In Europe there are many beautiful lakes, and people like to enjoy boat riding. But Krishna also likes to ride. In fact, He is the topmost rider! While the boat circles the pond, devotees from all sides offer different gifts to the Divine Couple: fruits, various food preparations, money and flowers. Then Sri Radha- Syamasundara take Their seat on another specially prepared asana, in the center of the pond. They are offered puja and varieties of food preparations. His Grace Mukunda Datta Prabhu {ACBSP} sings the bhajan “Radha Krishna prana mora…”, and after that there is a dance offered by Chandramukhi Dasi. Like in the spiritual world, everything is done for Their Lordships’ satisfaction! The festival ends by distributing delicious maha prasada to all the devotees!</p>

