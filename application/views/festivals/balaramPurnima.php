                            <p  style="font-size: 15px; text-align: justify;">Balaram, the elder brother of Vrajendranandan Shri Krishna, is the very first expansion of Krishna and is the embodiment of the Sat or Sandhini shakti, which is the potency of existence of the Lord. He, in the form of Sankarshan, creates all the Tatastha jivas present in this material world. Lord Balaram serves Lord Krishna in all the rasas by expanding himself in the form of his paraphernalia to serve him in Shanta ras; in the form of Lakshman, the younger brother of Lord Ram, he served Lord Ram in Dasya ras, always serving him in all ways possible; in the form of Balaram and Nityananda prabhhu he serves the lord in Sakhya ras and also in Vatsalya ras as he is the elder brother and also has parental affection towards Krishna with a feeling to protect him and take care of him. Lord Balaram also serves the lord in Madhurya ras, in the form of Ananga manjari, the younger sister of Shrimati Radharani. In Radha kund there is a kunj in the centre of the kund named Svananda sukhada kunj, which belongs to Ananga manjari, where she serves Radha Shyamsundar and makes many arrangements for them to relish transcendental pleasure in performing conjugal pastimes.<br><br>

                            Lord Balaram carries a plough, the reason for which is to cultivate the hearts of the devotees so that it becomes fertile and suitable for the seed of Bhakti to grow nicely. Being the Adi guru tattve, he guides the devotees on their spiritual path so that they can go back home back to godhead.<br><br>

                             On the Purnima in the month of Shravan the auspicious appearance day of Lord Balaram is celebrated by all the devotees in Krishna Balaram Mandir, Vrindavan. After a beautiful Mangal arati darshan with a new dress, all the devotees attend the morning program followed by Darshan arati, in which lord Balaram, dressed in a gorgeous new outfit, bedecked with jewels and covered in the forest flowers of Vrindavan, appears as cupid himself, piercing the hearts of all the assembled devotees with the arrows of his unmatched handsomeness.<br><br>

                             millions of moons, his sweet red lips playing a mischievous smile, his eyes mercifully glancing over all the assembled vaishnavas, and his curling locks entangling in them the hearts of all the onlookers, Lord Balaram submerges one and all in the ocean of his beauty. 

 
