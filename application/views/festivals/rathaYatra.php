                            <p  style="font-size: 15px; text-align: justify;">There are several stories associated with RathaYatra. Once in Dwarika, Lord Krishna's eight queens requested mother Rohini to narrate the divine episodes of Lord Krishna with the Gopi’s while he was in Vraj. For a while Rohini dithered. Finally, after a lot of insistence she relented. However, considering it unbecoming of Subhadra to hear such episodes (Leela), she sent her to guard the palace doorway. Yet the Vrajkatha soon absorbed Subhadra. Soon, Lord Shri Krishna and Balaram arrived at the doorway. With arms wide apart, she stood between the two, preventing them from entering. However, from where they stood, Rohini's katha soon engrossed them all! Just then sage Narad arrived. Seeing the siblings standing together like murtis, he humbly prayed, "May the three of you grant darshan in this manner forever." The Lord granted the boon. And the three eternally reside in the Jagannath Mandir in Puri.<br><br>

                            Another story is - When Shri Krishna was being cremated in Dwarika, Balaram, overpowered with grief, dashed into the ocean with Shri Krishna's partially cremated body. Subhadra too, followed both the brothers. At the same time, on the eastern shore of India, King Indradyumna of Jagannath Puri had a dream that the Lord's body would float up to the shores of Puri. He should build a huge mandir in the city and consecrate the wooden murtis of Shri Krishna, Balaram and Subhadra. The bones(asthi) of Lord Krishna's body should be placed in the hollow in the back of the murti. The dream came true. He found the splinters of bone (asthi) and took them <br><br>

                            But the question was who would carve the murtis. It is said that the architect of the gods - Vishwakarma - arrived as an old carpenter. He stipulated that while carving the murtis nobody should disturb him and if anybody did, he would stop work and leave. <br><br>

                            A few months elapsed. Driven with impatience, Indradyumna opened the door of Vishwakarma's room, who vanished instantly as he had stipulated. Despite the incomplete murtis, the king consecrated them, placing the holy cinders of Lord Krishna in the hollow of the murti and installed them in the mandir. 

