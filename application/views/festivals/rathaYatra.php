                    <p  style="font-size: 15px; text-align: justify;"><p  style="font-size: 15px; text-align: justify;"> Traditionally Only Hindu’s are allowed to go inside the temple and have darshan of the Lord. Hence,SrilaPrabhupada started Ratha Yatra in ISKCON all over the world so that everyone can receive blessings of Lord Jagannath, Baladev and Subhadra. The first Ratha yatra outside India was initiated by A. C. Bhaktivedanta Swami Prabhupada (founder of ISKCON) on July 9, 1967 in San Francisco. The Lord of the Universe (Lord Jagannath) now parade in major cities all over the world such as London, Paris, New York, Durban, Rome, etc. People around the globe join the joyful and colourfulRatha yatra festival. Thousands throng the streets to pull the giant chariots, chant and dance and enjoy tasty prasad given to all joining in a celebration to honour the appearance of Lord Jagannath, Baladev and Subhadra.<br><br>


                     The Ratha yatra festival has been traditionally celebrated annually in Jagannath Puri, Orrisa, where Lord Jagannath along with His sister Subhadra and elder brother Baladeva paraded through the streets of the city. This practice and festival continue to take place in Puri and since, late sixties all over the world. The pulling of Rath cart during the Rath yatra procession by the devotees symbolizes the attempt of the residents of Vrindavan, especially the cowherd boys and girls, to bring Krishna, Balarama and Subhadra back to Vrindavan.<br><br>
                            
                    rathe ca vamanamdrstvapunarjanmanavidyate: Simply by seeing the Lord on the chariot, one makes advancement for stopping the repetition of birth and death. To give this opportunity to everyone in the locality, we celebrate the Ratha Yatra of Sri Sri Krishna Balarama every year in the month of January. The utsavadeities of Krishna and Balarama ride on their chariots to bless everyone. The chariot is decorated with a colourful display of lights and flowers. The decorative light fittings are a sight to behold. Thousands of devotees pull the chariot, offer their prayers, join thekirtana of soulful singing and distribute prasad, all along the route of the Ratha Yatra.
                    <br><br>
                            
                    ISKCON Vrindavan celebrates Rath yatra annually around October/November. This time it is on 30th October, 2019. The festivities include the Rath yatra, offering of Chappanbhoga (56 varities), spiritual discourses along with spiritual and cultural festival.
