                           <p  style="font-size: 15px; text-align: justify;"> The ocean of the endless glories of Nitai is then discussed till it is time for the Abhishek of their Lordships Gaur Nitai. The Lords shower the Vaishnavas with unlimited transcendental bliss during this bathing ceremony which is accompanied by Kirtan enthused with Krishna prem. This beautiful bathing ceremony is followed by a lavish bhoga offering to the Lords prepared by devotees with the desire to please Nitai. Following the aratik that is offered to the deities after the bhoga offering, is a feast for all the devotees, who had been fasting till noon to celebrate the occasion. In this way Nityananda Trayodashi is beautifully observed in ISKCON Vrindavan.</p><br>


