                            <br>
                            <p  style="font-size: 15px; text-align: justify;">On Trayodashi tithi of the auspicious Magha maas(feb-march), just after Vasant Panchami, when the earth is covered in beautiful and fragrant flowers blooming everywhere, the moon of Nityananda ray arose from the womb of Devi Padmavati in the most sanctified village of Ekachakra, giving unlimited bliss to Hadai Pandit, lord Nityananda’s father and all the other Vaishnavas. Lord Nityananda performed his inconceivably enchanting and amazing pastimes first in Ekachakra, after which he travelled to various places of pilgrimage and then joined shri Chaitanya Mahaprabhu in Shridham Mayapur and together the two brothers drowned the entire universe in the loud and ecstatic chanting of the Hare Krishna Mahamantra.<br><br><br>

                            Lord Nityananda is even more merciful than the munificent Lord Gaurahari and the two lotus feet of his, which are as cooling as million of moons(nitai-pada-kamala, koti-candra-susitala) are the only shelter for the aspiring devotees of Lord Gaurahari in this age of Kali, which is full of miseries. Lord Nityananda very mercifully enundated the entire universe with Krishna Prem and distributed love of good head without considering whether one is qualified or not.  Jahnavapati Nityananda ram is Lord Balaram himself(balarāma hoilo nitāi) and is akhanda guru tattva, he is the one who shows the devotee the path to spiritual advancement, without his mercy one cannot even hope to attain the lotus feet of Gaurangadev.<br><br><br>

                             On the divine appearance day of Lord Nityananda all the devotees assemble in the Krishna Balaram mandir in Shri Vrindavan dham for the morning program. At 7:30, when the altars open, lord Nitai, exquisitely dressed in a new outfit and bedecked with beautiful ornaments and flowers, steals away the hearts of all the devotees gathered there to receive a drop of mercy from the most merciful Nitai.
