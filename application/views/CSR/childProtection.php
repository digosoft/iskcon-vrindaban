                        Started March 15th, 2015, more than 100,000 Brajvasis, devotees, farmers and people from various organizations marched from Vrindavan to Delhi to stop the abuse of the Yamuna river. This “Pada Yatra” represented a turning point in the popular Save Yamuna campaign and took place under the name “Yamuna Muktikaran Abhiyan”.<br><br>


                        The INCSR has so far revived over 50+ kunds and has created an additional water retention capacity of five lakh cubic metres. Due to the limitation of funds, we cannot scale up our activities. Hence, we have to prioritise the kunds on the basis of their viability and historical significance.
